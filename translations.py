def get_text(key: str, lang: str = 'en', **kwargs) -> str:
    """
    Get translated text for a given key.
    
    Args:
        key: Translation key
        lang: Language code (en, pt, es, fr, it, de)
        **kwargs: Format variables for string interpolation
        
    Returns:
        Translated text with variables interpolated
    """
    text = TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS['en'].get(key, key))
    
    # Handle string formatting
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    
    return text


